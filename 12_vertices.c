void orienta_torneio (int n_torneio) {
	int i, j, cont = 0;
	
	for(i = 0; i < 7; i++){
		for(j = i + 1; j < 7; j++){
            if(NISOT7[cont + (21 * n_torneio)] == 0){
                t114[i][j]= 1;
                t114[j][i]= -1;
            }
            else{
                t114[i][j] = -1;
                t114[j][i] = 1;
            }
            
            cont++;
        }
    }	
}

